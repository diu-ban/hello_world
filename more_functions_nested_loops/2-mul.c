#include "main.h"

/**
 * mull - Multiplies two integers
 * @a: The first integer
 * @b: The second integer
 *
 * Description: This function takes two integers as arguments and
 *              returns their product.
 *
 * Return: The product of a and b
 */
int mull(int a, int b)
{
	return (a * b);
}
